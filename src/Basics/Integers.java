package Basics;

public class Integers {
    public static void main(String[] args) {
        int x = 200;
        int y = 58;
        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y); // integer input will always give integer output
    }
}
